package week_1_if_else;

public class Temp {
    public static void main(String[] args) {
        double tempYesterday = 100;
        double tempToday = 65;

        if (tempYesterday > tempToday) {
            System.out.println("Yesterday was warmer");
        } else {
            System.out.println("Today is warmer");
        }
    }
}
